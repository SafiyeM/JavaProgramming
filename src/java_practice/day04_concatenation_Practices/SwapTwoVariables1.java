package java_practice.day04_concatenation_Practices;

public class SwapTwoVariables1 {

    public static void main(String[] args) {

        int x= 10;
        int y= 15;

        int z; // our temporary third variable to swap two variables x & y

        z=x; // in this line z= 10 x=10 y=15
        x=y; // in this line z= 10 x=15 y=15
        y=z; // in this line z= 10 x=15 y=10

        System.out.println("x= ");






    }
}
