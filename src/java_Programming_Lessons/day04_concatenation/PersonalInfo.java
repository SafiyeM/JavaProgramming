package java_Programming_Lessons.day04_concatenation;

public class PersonalInfo {
    public static void main(String[] args) {

        String firstName = "Aaron",
                lastName = "Daniel",
                from = "United Kingdom",
                favorite_music = "Rock music",
                favorite_book = "Data Structures in Java",
                favoriteTV_show = "Walking Dead",
                favorite_color = "blue";

        char gender = 'M';

        int age = 30;

        boolean isMarried = true,
                isEmployed = false;

        System.out.println("firstName = " + firstName);
        System.out.println("lastName = " + lastName);
        System.out.println("gender = " + gender);
        System.out.println("age = " + age);
        System.out.println("from = " + from);
        System.out.println("favorite_music = " + favorite_music);
        System.out.println("favorite_book = " + favorite_book);
        System.out.println("favoriteTV_show = " + favoriteTV_show);
        System.out.println("favorite_color = " + favorite_color);
        System.out.println("isMarried = " + isMarried);
        System.out.println("isEmployed = " + isEmployed);




    }


}
