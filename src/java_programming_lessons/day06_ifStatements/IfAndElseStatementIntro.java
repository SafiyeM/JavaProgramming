public class IfAndElseStatementIntro {

    public static void main(String[] args) {

// if else statement

        int age = 28;

        if (age >= 21){
            System.out.println("eligible");
        }

        if(age < 21) {
            System.out.println("not eligible");
        }


        System.out.println("__________________________________________");

        if (age >= 21){
            System.out.println("eligible");
        } else { //otherwise
            System.out.println("not eligible");
        }









    }
}
