package java_programming_lessons.day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {


        System.out.println("Hello Students\nToday we have Java class");

        System.out.println("________________________________________");

        System.out.println("\tJava Programming");
        System.out.println("\t\tJava Developer course Batch 29");

        System.out.println("_______________________________________");

        System.out.println("\\");

        System.out.println("________________________________________");

        System.out.println("My favorite TV show is \"Snowpiercer\"");

    }
}

/*
1. \n: New Line
2. \t: paragraph space
3. \\: Back slash
4. \": double quote


 */